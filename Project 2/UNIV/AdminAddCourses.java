    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
        {
            FileReader fr = new FileReader(jTextField6.getText());
            BufferedReader br = new BufferedReader(fr);
            
            String line;
            
            while((line = br.readLine()) != null)
            {
                
                ArrayList<Course> prereqList = new ArrayList<>();
                
                for (String splitString : line.split(",", -1)[4].split(":", -1)) 
                {
                    try
                    {      
                        if(!line.split(",", -1)[4].isEmpty())
                        {
                            prereqList.add(catalog.findCourse(splitString));
                        }
                    }
                    catch(NullPointerException e)
                    {
                        JOptionPane.showMessageDialog(this, "You are adding a course with a prerequisite that doesn't exist in the catalog!\nHalting database populating process...");
                        c.deleteAllCourses();
                        admin.UpdateCourseList();
                        this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
                        return;
                    }

                }
                
                catalog.addCourse(new Course(line.split(",", -1)[0], Double.parseDouble(line.split(",", -1)[1]), line.split(",", -1)[2], line.split(",", -1)[3], prereqList));
            }
            
            br.close();
            fr.close();
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(this, "There was a problem loading the courses");
            System.out.println(e);
        }
        
        admin.UpdateCourseList();
        this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_jButton2ActionPerformed

