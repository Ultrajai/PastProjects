    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "One or more fields are empty!");
        }
        else
        {
            ArrayList<Course> prereqList = new ArrayList<>();
        
            if(!jTextField5.getText().isEmpty())
            {
                for (String splitString : jTextField5.getText().split(":", -1)) 
                {
                    try
                    {      
                        prereqList.add(catalog.findCourse(splitString));
                    }
                    catch(NullPointerException e)
                    {
                        JOptionPane.showMessageDialog(this, "You are adding a course with a prerequisite that don't exist in the catalog!");
                    }

                }
            }
            Course toAdd = new Course(jTextField1.getText(), Double.parseDouble(jTextField2.getText()), jTextField3.getText(), jTextField4.getText(), prereqList);
            catalog.addCourse(toAdd);
        }
        
        admin.UpdateCourseList();
        this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_jButton1ActionPerformed

